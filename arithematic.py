def add(a,b):
    return a+b
def subtract(a,b):
    return a-b
def multiply(a,b):
    return a*b
def division(a,b):
    return a/b
def modulus(a,b):
    return a%b
print("addition:",add(10,5))
print("substraction:",subtract(10,5))
print("multiplication:",multiply(10,5))
print("division:",division(10,5))
print("modulus:",modulus(10,5))